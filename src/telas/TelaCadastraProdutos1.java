    /**
     * Carregar os tipos do banco na tela
     */
    private void carregarTipos() {

        try {
            Tipo tipo = new Tipo();
            DefaultTableModel modelo = new DefaultTableModel();
            ArrayList<Tipo> listaTipos = dadosTipo.listarTipo(tipo);

            modelo.setColumnIdentifiers(new Object[]{"Código", "Tipo"});
            for (Tipo t : listaTipos) {
                modelo.addRow(new Object[]{t.getCod_tipo(), t.getDescricao()});
            }
            jtTipos.setModel(modelo);

        } catch (Exception ex) {
            //Logger.getLogger(TelaCadastraProdutos1.class.getName()).log(Level.SEVERE, null, ex);

        }

    }

    /**
     * Lista os produtos do banco na tela
     */
    private void carregarProdutos() {

        try {
            Produto produto = new Produto();

            ArrayList<Produto> listaProdutos = new ArrayList<>();
            listaProdutos = dadosProduto.listar(produto);
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.setColumnIdentifiers(new Object[]{"Código", "Descrição", "Tipo", "Disponibilidade", "Valor(R$)"});
            for (Produto p : listaProdutos) {
                modelo.addRow(new Object[]{p.getCod_produto(), p.getNome(), p.getTipo().getDescricao(), p.getDisponibilidade(), p.getValorUnitario()});
            }
            jtProdutos.setModel(modelo);
            
            //Coloca a tabela na ordem que clicar na coluna
            jtProdutos.setAutoCreateRowSorter(true);
            

        } catch (Exception ex) {
            //Logger.getLogger(TelaCadastraProdutos1.class.getName()).log(Level.SEVERE, null, ex);

        }

    }

