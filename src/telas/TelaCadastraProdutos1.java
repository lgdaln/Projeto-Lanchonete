    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:

        try {

            Produto p = new Produto();
            int linha = jtProdutos.getSelectedRow();
            p.setCod_produto((int) jtProdutos.getValueAt(linha, 0));
            p.setNome((String) jtProdutos.getValueAt(linha, 1));
            p.setValorUnitario((double) jtProdutos.getValueAt(linha, 4));

            if ((jtProdutos.getValueAt(linha, 3)).equals("Sim")) {
                jrbDisponivel.setSelected(true);
                jrbIndisponivel.setSelected(false);
            } else {
                jrbDisponivel.setSelected(false);
                jrbIndisponivel.setSelected(true);
            }

            //Rotina para selecionar na tabela de tipos que corresponde ao tipo do produto que será alterado.
            String nomeTipoTabelaProduto = (String) jtProdutos.getValueAt(linha, 2);
            int tamanho = jtTipos.getRowCount();
            for (int i = 0; i <= tamanho; i++) {

                String nomeTipoTabelaTipo = (String) jtTipos.getValueAt(i, 1);

                if (nomeTipoTabelaProduto.equals(nomeTipoTabelaTipo)) {
                    jtTipos.getSelectionModel().addSelectionInterval(i, i);
                    break;

                }
            }

            jtfCodigo.setText(String.valueOf(p.getCod_produto()));
            jtfProduto.setText(p.getNome());
            jtfPreco.setText(String.valueOf(p.getValorUnitario()));

        } catch (Exception ex) {
            Logger.getLogger(TelaCadastraProdutos1.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbSalvarAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarAlteracaoActionPerformed
        // TODO add your handling code here:

        try {

            produto.setCod_produto(Integer.parseInt(jtfCodigo.getText()));
            produto.setNome(jtfProduto.getText());

            if (jrbDisponivel.isSelected()) {
                produto.setDisponibilidade("Sim");
            } else {
                produto.setDisponibilidade("Faltando");
            }

            produto.setValorUnitario(Double.valueOf(jtfPreco.getText()));

            int linhaTipo = jtTipos.getSelectedRow();
            int codigoTipo = (int) jtTipos.getValueAt(linhaTipo, 0);
            String descricao = (String) jtTipos.getValueAt(linhaTipo, 1);

            tipo.setCod_tipo(codigoTipo);
            tipo.setDescricao(descricao);

            produto.setTipo(tipo);

            regrasProduto.editarProduto(produto);

            carregarProdutos();

            limparCampos();

        } catch (Exception ex) {
            //Logger.getLogger(TelaCadastraProdutos1.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jbSalvarAlteracaoActionPerformed

